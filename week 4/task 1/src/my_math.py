"""
my_math.py
----------
A custom module with basic math functions.
This module is imported and used in main.py.
"""

def add(a, b):
    """Returns the sum of two numbers."""
    return a + b

def subtract(a, b):
    """Returns the difference of two numbers (a - b)."""
    return a - b

def multiply(a, b):
    """Returns the product of two numbers."""
    return a * b
